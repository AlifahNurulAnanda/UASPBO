    private void btn_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpanActionPerformed
        // TODO add your handling code here:
        String jenis_kelamin = null;
        if(btn_laki.isSelected()){
            jenis_kelamin ="Laki-Laki";

        }else if(btn_perempuan.isSelected()){
            jenis_kelamin = "Perempuan";        
        }
        String sql = "insert into karyawan values (?,?,?,?,?,?)";
        try{
            PreparedStatement stat = con.prepareStatement(sql);
            stat.setString(1, txt_id.getText());
            stat.setString(2, txt_nama.getText());
            stat.setString(3, jenis_kelamin);
            stat.setString(4, txt_notelp.getText());
            stat.setString(5, txt_alamat.getText());
            stat.setString(6, txt_jabatan.getText());
            
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "data berhasil di simpan");
            kosong();
            txt_id.requestFocus();



        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "data gagal di simpan " + e);
        }
        datatable();
    
    }//GEN-LAST:event_btn_simpanActionPerformed

