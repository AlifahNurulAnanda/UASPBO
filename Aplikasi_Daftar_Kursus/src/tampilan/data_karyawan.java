    protected void datatable(){
        Object[] Baris = {"ID Karyawan", "Nama Karyawan", "Jenis Kelamin", "No Telepon", "Alamat", "Jabatan"};
        tabmode = new DefaultTableModel(null, Baris);
        
        String cariitem = txt_cari.getText();
        
        try{
            String sql = "SELECT * FROM karyawan where id_karyawan like '%"+cariitem+"%' or nama_karyawan like '%"+cariitem+"%' order by id_karyawan asc";
            Statement stat = con.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()){
                tabmode.addRow(new Object[]{
                hasil.getString(1),
                hasil.getString(2),
                hasil.getString(3),
                hasil.getString(4),
                hasil.getString(5),
                hasil.getString(6)
            });
            }
             tabel_karyawan.setModel(tabmode);
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "data gagal di panggil " + e);
            
        }
        
    }

